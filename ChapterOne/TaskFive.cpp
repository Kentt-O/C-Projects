#include <iostream>

using namespace std;
int main() {
    cout << "Program to complete and output the perimeter and "
    << "area of a rectangle." << endl;

    double length = 6.0;
    double width = 4.0;
    double perimeter = 2 * (length * width);
    double area = length * width;

    cout << "Length = " << length << endl;
    cout << "Width = " << width << endl;
    cout << "Perimeter = " << perimeter << endl;
    cout << "Area = " << area << endl;

    return 0;
}
